    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        String numH = numero_historia.getText();
        if(numH.equals("")){
            JOptionPane.showMessageDialog(null, "Ingrese el número de la historia e intente nuevamente");
        }else{
            ArrayList<String[]> lista = new ArrayList();
            lista = contPaciente.consultarHistoria(numH);
            
            jTable1.setModel(modeloTabla = new DefaultTableModel(
            new Object [][] {

            },
            new String [] {
                "Medico", "Causa", "Fecha"
            }
        ) {
            Class[] types = new Class [] {
                java.lang.String.class, java.lang.String.class, java.lang.String.class
            };
            boolean[] canEdit = new boolean [] {
                false, false, false
            };

            public Class getColumnClass(int columnIndex) {
                return types [columnIndex];
            }

            public boolean isCellEditable(int rowIndex, int columnIndex) {
                return canEdit [columnIndex];
            }
        });
        
        for(int i = 0; i < lista.size(); i++){
        
            String medico = lista.get(i)[0];
            String causa = lista.get(i)[1];
            String fecha_hora = lista.get(i)[2];
            
            
            modeloTabla.addRow(new Object[]{medico,causa, fecha_hora});
           
        
        
        }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

