    public void consultarCantidadMes() throws SQLException{
    
        int cantidad = 0;
        
        String mes = (String)comboMeses.getSelectedItem();
        String year = (String)comboAnos.getSelectedItem();
        String idMedico = ventAdminMedi.getSesion().getIdentificacion();
        
        
        
        switch(mes){
        
            case "Enero": 
                
                cantidad  = daoMedico.contarCosultas(idMedico,year,"01","31");

            break;
        
            case "Febrero": 
                
                cantidad  = daoMedico.contarCosultas(idMedico,year,"02","28");

            break;
            
            case "Marzo": 

                cantidad  = daoMedico.contarCosultas(idMedico,year,"03","31");
                
            break;
            
            case "Abril": 

                cantidad  = daoMedico.contarCosultas(idMedico,year,"04","30");
                
            break;
            
            case "Mayo": 
                
                cantidad  = daoMedico.contarCosultas(idMedico,year,"05","31");

            break;
           
            case "Junio": 
                
                cantidad  = daoMedico.contarCosultas(idMedico,year,"06","30");

            break;
                       
            case "Julio": 

                cantidad  = daoMedico.contarCosultas(idMedico,year,"07","31");
                
            break;
                       
            case "Agosto": 
                
                cantidad  = daoMedico.contarCosultas(idMedico,year,"08","31");

            break;
                        
            case "Septiembre": 

                cantidad  = daoMedico.contarCosultas(idMedico,year,"09","30");
                
            break;
                              
            case "Octubre": 

                cantidad  = daoMedico.contarCosultas(idMedico,year,"10","31");
                
            break;
            
            case "Nomvienbre": 

                cantidad  = daoMedico.contarCosultas(idMedico,year,"11","30");
                
            break;
                    
            case "Diciembre": 

                cantidad  = daoMedico.contarCosultas(idMedico,year,"12","31");
                
            break;
        
        }
        
        
        
        campoTotalCOnsultas.setText(String.valueOf(cantidad));
        
    }
    
