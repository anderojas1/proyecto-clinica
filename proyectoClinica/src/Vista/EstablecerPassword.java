    private void buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarActionPerformed
        
        String id = identificacion.getText();
        
        Validador validar = new Validador();
        
        try {
            
            validar.validarCampo(id);
            
            driver = new DriverPersona();
            
            usuario = driver.consultarPersona(id);
            
            JOptionPane.showMessageDialog(this, "Se encontró al usuario con identificación " + usuario.getIdentificacion(), 
                    "Consulta exitosa", JOptionPane.INFORMATION_MESSAGE);
            
            identificacion.setEnabled(false);
            buscar.setEnabled(false);
            
            pass1.setEnabled(true);
            pass2.setEnabled(true);
            
            guardar.setEnabled(true);
            
        } catch(ExcepcionCamposVacios ex) {
            
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Campos requerios sin llenar", JOptionPane.ERROR_MESSAGE);
            
        } catch (SQLException ex) {
            
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Error en la consulta", JOptionPane.ERROR_MESSAGE);
            
        } catch (NullPointerException ex) {
            
            JOptionPane.showMessageDialog(this, "No se encontró usuario con la identificación proporcionada", 
                    "Usuario no existe", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_buscarActionPerformed

