    public void cargarEmpleados() throws SQLException{
    
        ArrayList<String[]> listaEmpleados = new ArrayList();
        
        
        String datosArea = (String)comboAreas.getSelectedItem();
        String [] datosAreaPatidos = datosArea.split(",");
        
        
        listaEmpleados = driverEmpleado.consultarEmpleadosArea(datosAreaPatidos[0]);
        
        for(int i=0; i<listaEmpleados.size(); i++){
            
            String ident = listaEmpleados.get(i)[0];
            String nombre = listaEmpleados.get(i)[1];
            String salario = listaEmpleados.get(i)[2];
            String apellidos = listaEmpleados.get(i)[3];
            
            
            modeloTabla.addRow(new Object [] {ident,nombre,apellidos,salario});
        
        }
    }
    
