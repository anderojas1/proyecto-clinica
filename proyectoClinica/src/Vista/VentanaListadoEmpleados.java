    public void cargarAreas() throws SQLException{
    
        ArrayList <String[]> areas = new ArrayList();
        
        areas = driverArea.consultarAreas();
        
        for(int i = 0; i < areas.size(); i++ ){
        
            comboAreas.addItem(areas.get(i)[0]+","+areas.get(i)[1]);
        }
    
    }
    
    
