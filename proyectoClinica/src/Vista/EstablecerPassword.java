    private void guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarActionPerformed
        
        String contraseña1 = new String(pass1.getPassword());
        String contraseña2 = new String(pass2.getPassword());
        
        String info [] = {contraseña1, contraseña2};
        
        try {
            
            Validador valida = new Validador();   
            
            valida.validarModulos(info);
            valida.validarContraseñas(contraseña1, contraseña2);
            
            driver.actualizarContraseña(usuario.getIdentificacion(), contraseña2);
            
            JOptionPane.showMessageDialog(this, "Contraseña cambiada exitosamente", "Nueva contraseña generada", 
                    JOptionPane.INFORMATION_MESSAGE);
            
            VentanaLogin newLogin = new VentanaLogin();
            newLogin.setLocationRelativeTo(null);
            newLogin.setVisible(true);

            dispose();
            
            
        } catch (PasswordNoMatchException | ExcepcionCamposVacios ex) {
            
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Campos requeridos", JOptionPane.ERROR_MESSAGE);
            
        } catch (SQLException ex) {
            
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Error en la actualización", JOptionPane.ERROR_MESSAGE);
            
        }
            
            
    }//GEN-LAST:event_guardarActionPerformed

   

